public class Comida //se declara la clase comida, un producto de la tiendita
{
	 String nombre; //se declara para dar un nombre al producto 	
	 int precio;	//se declara para asignarle el precio al producto
	 	//se declara el producto y se le asignan tanto nombre como precio
	 
	public Comida()
		{
			nombre = "queso";
			precio = 8;
		}
	public Comida(String nom,int p)
		{
	 	nombre = nom;
	 	precio = p;
		}
}
